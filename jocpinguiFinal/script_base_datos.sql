

-- 1. Tabla para gestionar los usuarios
CREATE TABLE USUARIO (
    NICKNAME VARCHAR2(50 BYTE) NOT NULL,
    CONTRASENA VARCHAR2(100 BYTE) NOT NULL,
    CONSTRAINT PK_USUARIO PRIMARY KEY (NICKNAME)
);

-- 2. Tabla para datos relacionales de la partida 
CREATE TABLE PARTIDA (
    ID_PARTIDA NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NUM_TURNOS NUMBER,
    JUGADOR_ACTUAL NUMBER,
    FECHA TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_PARTIDA PRIMARY KEY (ID_PARTIDA)
);

-- 3. Tabla relacional de los jugadores dentro de la partida
CREATE TABLE JUGADOR_PARTIDA (
    ID_PARTIDA NUMBER NOT NULL,
    NICKNAME VARCHAR2(50 BYTE) NOT NULL,
    POSICION NUMBER,
    COLOR VARCHAR2(20 BYTE),
    INVENTARIO VARCHAR2(4000 BYTE),
    CONSTRAINT PK_JUGADOR_PARTIDA PRIMARY KEY (ID_PARTIDA, NICKNAME),
    CONSTRAINT FK_JP_PARTIDA FOREIGN KEY (ID_PARTIDA) REFERENCES PARTIDA(ID_PARTIDA) ON DELETE CASCADE,
    CONSTRAINT FK_JP_USUARIO FOREIGN KEY (NICKNAME) REFERENCES USUARIO(NICKNAME) ON DELETE CASCADE
);

-- 4. Tabla de guardado completo que pide tu GestorPartida.java (BLOB)
CREATE TABLE PARTIDAS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    nombre VARCHAR2(100) NOT NULL,
    usuario VARCHAR2(50) NOT NULL,
    datos BLOB NOT NULL,
    fecha_creacion DATE DEFAULT SYSDATE,
    CONSTRAINT PK_PARTIDAS PRIMARY KEY (id)
);
